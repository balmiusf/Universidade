package Oceano;

public class Run{
	String id;
	int size;
	
	public Run(String id,int size){
		this.id = id;
		this.size = size;
	}
	
	public String getRun(){
		return id;
	}
	public int getSize(){
		return size;
	}
	public void setSize(int size){
		this.size = size;
		
	}

}
